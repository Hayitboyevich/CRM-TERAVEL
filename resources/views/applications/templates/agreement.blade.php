<p style="text-indent:35.45pt; text-align:justify; font-size:12pt">
    <strong><span style="font-family:''; ">Заказчик </span></strong><span style="font-family:''">– физическое или юридическое лицо, заказывающее туристический продукт от имени туриста, в том числе законный представитель несовершеннолетнего туриста. Везде, где по тексту настоящего Договора указан Заказчик, имеются ввиду также и третьи лица, в интересах которых он действует, сопровождающие его или сопровождаемые им лица.</span>
</p>
<p style="text-indent:35.45pt; text-align:justify; font-size:12pt">
    <strong><span style="font-family:''; ">Туристический продукт (далее-Турпродукт)</span></strong><span style="font-family:''"> - комплекс услуг по перевозке и размещению, оказываемых за определенную цену по настоящему Договору.</span>
</p>
<p style="text-indent:35.45pt; text-align:justify; font-size:12pt">
    <strong><span style="font-family:''; ">Средство размещения</span></strong><span style="font-family:''"> – отель, апарт-отель, апартаменты, пансионат, санаторий, турбаза, дом отдыха, кемпинг, хостел или иное средство временного проживания, предлагающее услуги по размещению, питанию, а также иные, имеющие отношения к гостиничной сфере, услуги.</span>
</p>
<p style="text-indent:35.45pt; text-align:justify; font-size:12pt">
    <strong><span style="font-family:''; ">Номер </span></strong><span style="font-family:''">– помещение в Гостинице (а в применимых случаях также дом (коттедж) и их части, апартаменты, кровать/койку-место в общих помещениях и прочее), которое является объектом бронирования.</span>
</p>
<p style="text-indent:35.45pt; text-align:justify; line-height:12pt; background-color:#ffffff">
    <strong><span style="font-family:''; font-size:12pt; ">Заказ</span></strong><span style="font-family:''; font-size:12pt"> — заявка на получение турпродукта, оформляемая Исполнителем в порядке и на условиях, предусмотренных настоящим Договором.</span>
</p>
<p style="text-indent:35.45pt; text-align:justify; line-height:98%; font-size:12pt">
    <strong><span style="font-family:''; ">Ваучер</span></strong><span style="font-family:''"> — электронный документ, подтверждающий право Заказчика на получение Услуг, выбранных им согласно оформленного и оплаченного Заказа.</span>
</p>
<p style="text-indent:35.45pt; text-align:justify; line-height:98%; font-size:12pt">
    <strong><span style="font-family:''; ">Бронирование</span></strong><span style="font-family:''"> – индивидуальная заявка или заявки на резервирование турпродукта, которая оплачена и подтверждена отдельной проводкой бронирования. </span>
</p>
<p style="text-indent:35.45pt; text-align:justify; line-height:98%; font-size:12pt">
    <strong><span style="font-family:''; ">Аннулирование</span></strong><span style="font-family:''"> – отмена Бронирования турпродукта, осуществленное ранее. </span>
</p>
<p style="text-indent:35.45pt; text-align:justify; line-height:98%; font-size:12pt">
    <strong><span style="font-family:''; ">Изменение</span></strong><span style="font-family:''"> – изменение условий ранее заказанных и подтвержденных турпродуктов. </span>
</p>
<p style="text-indent:35.45pt; text-align:justify; font-size:12pt">
    <strong><span style="font-family:''; ">Возвратный тариф</span></strong><span style="font-family:''"> - стоимость турпродукта, отмена Заказа которой допускается Исполнителем вплоть до Даты наступления штрафных санкций. </span>
</p>
<p style="text-indent:35.45pt; text-align:justify; font-size:12pt">
    <strong><span style="font-family:''; ">Невозвратный тариф</span></strong><span style="font-family:''"> - стоимость турпродукта, отмена Заказа которой Исполнителем не допуска</span><span style="font-family:''">&#xad;</span><span style="font-family:''">ется. В случае отмены Заказчиком Заказа на турпродукт по Невозвратному тарифу стоимость турпродукта возврату не подлежит ни при каких обстоятельствах. В исключительных случаях (при наличии возможности) Заказ на турпродукт по Невозвратному тарифу может быть отменен или изменен, при этом порядок и условия такой отмены или изменения обговариваются Сторонами отдельно. </span>
</p>
<p style="text-indent:35.45pt; text-align:justify; line-height:98%; font-size:12pt">
    <strong><span style="font-family:''; ">Дата наступления штрафных санкций</span></strong><span style="font-family:''"> - дата, до наступления которой Заказ на турпродукт по Возвратному тарифу может быть отменен Заказчиком, и после наступления которой Заказ на турпродукт по Возвратному тарифу автоматически становится Заказом на турпродукт по Невозвратному тарифу. Оформляя Заказ на турпродукт по Возвратному тарифу, Заказчик подтверждает свое ознакомление и согласие с Датой наступления штрафных санкций.</span>
